CREATE OR REPLACE PROCEDURE get_user_engagement(IN input_user_id INT)
LANGUAGE plpgsql
AS $$
DECLARE
    post_count INT;
    like_count INT;
    comment_count INT;
BEGIN
    -- Posts by user
    SELECT COUNT(*) INTO post_count
    FROM Posts
    WHERE user_id = input_user_id;

    -- Likes by user
    SELECT COUNT(*) INTO like_count
    FROM Likes
    WHERE user_id = input_user_id;

    -- Comments by user
    SELECT COUNT(*) INTO comment_count
    FROM Comments
    WHERE user_id = input_user_id;

    -- Output results
    RAISE NOTICE 'User ID: %, Posts: %, Likes: %, Comments: %',
                 input_user_id, post_count, like_count, comment_count;
END;
$$;

