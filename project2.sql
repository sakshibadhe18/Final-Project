-- Query
--1.Views for Top Posts & Engagement Score

CREATE VIEW post_engagement AS
SELECT 
    p.post_id,
    p.content,
    u.username,
    COUNT(DISTINCT l.like_id) AS likes,
    COUNT(DISTINCT c.comment_id) AS comments,
    (COUNT(DISTINCT l.like_id) + COUNT(DISTINCT c.comment_id)) AS engagement_score
FROM posts p
LEFT JOIN users u ON p.user_id = u.user_id
LEFT JOIN likes l ON p.post_id = l.post_id
LEFT JOIN comments c ON p.post_id = c.post_id
GROUP BY p.post_id, p.content, u.username;

SELECT * FROM post_engagement;

--2. Window Function: Rank Posts
SELECT *,
       RANK() OVER (ORDER BY engagement_score DESC) AS rank
FROM post_engagement;

--3.  Trigger to Update like_count
CREATE OR REPLACE FUNCTION update_like_count()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE posts SET like_count = like_count + 1
    WHERE post_id = NEW.post_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger on insert into likes
CREATE TRIGGER trg_update_like_count
AFTER INSERT ON likes
FOR EACH ROW
EXECUTE FUNCTION update_like_count();

--4.  Top 5 Most Liked Posts
SELECT post_id, content, like_count
FROM Posts
ORDER BY like_count DESC
LIMIT 5;

--5.Most Active Users 
SELECT u.user_id, u.username,
       COUNT(DISTINCT p.post_id) AS total_posts,
       COUNT(DISTINCT c.comment_id) AS total_comments,
       COUNT(DISTINCT l.like_id) AS total_likes_given,
       (COUNT(DISTINCT p.post_id) + COUNT(DISTINCT c.comment_id) + COUNT(DISTINCT l.like_id)) AS total_activity
FROM Users u
LEFT JOIN Posts p ON u.user_id = p.user_id
LEFT JOIN Comments c ON u.user_id = c.user_id
LEFT JOIN Likes l ON u.user_id = l.user_id
GROUP BY u.user_id, u.username
ORDER BY total_activity DESC;

--6.Top 3 Commented Posts
SELECT p.post_id, p.user_id, p.content, COUNT(c.comment_id) AS comment_count
FROM Posts p
JOIN Comments c ON p.post_id = c.post_id
GROUP BY p.post_id
ORDER BY comment_count DESC
LIMIT 3;

--7. User-wise Average Engagement Per Post
SELECT u.user_id, u.username,
       AVG(COALESCE(pe.engagement_score, 0)) AS avg_engagement_score
FROM Users u
LEFT JOIN Posts p ON u.user_id = p.user_id
LEFT JOIN post_engagement pe ON p.post_id = pe.post_id
GROUP BY u.user_id, u.username
ORDER BY avg_engagement_score DESC;

--8.Posts Without Any Likes or Comments
SELECT p.post_id, p.content
FROM Posts p
LEFT JOIN Likes l ON p.post_id = l.post_id
LEFT JOIN Comments c ON p.post_id = c.post_id
WHERE l.like_id IS NULL AND c.comment_id IS NULL;

--9. Daily Engagement Report
SELECT DATE(p.created_at) AS date,
       COUNT(DISTINCT l.like_id) AS likes,
       COUNT(DISTINCT c.comment_id) AS comments,
       (COUNT(DISTINCT l.like_id) * 2 + COUNT(DISTINCT c.comment_id)) AS engagement_score
FROM Posts p
LEFT JOIN Likes l ON p.post_id = l.post_id
LEFT JOIN Comments c ON p.post_id = c.post_id
GROUP BY DATE(p.created_at)
ORDER BY date;

